package com.tantv.vnradiotruyen.adapter;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

import java.util.ArrayList;

/**
 * Created by tantv on 16/10/2015.
 */
public class PlayMusicFragmentAdapter extends FragmentPagerAdapter {
    ArrayList<Fragment> mFragments;

    public PlayMusicFragmentAdapter(FragmentManager fm, ArrayList<Fragment> mFragments) {
        super(fm);
        this.mFragments = mFragments;
    }

    @Override
    public Fragment getItem(int position) {

        return mFragments.get(position);
    }

    @Override
    public int getCount() {
        return mFragments.size();
    }
}
